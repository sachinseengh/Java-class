public class Q3_Conversion {
        public static void main(String[] args) {
            // Converting primitive types to wrapper objects
            int intValue = 42;
            Integer integerObject = intValue; // Auto-boxing
    
            double doubleValue = 3.14;
            Double doubleObject = doubleValue; // Auto-boxing
    
            char charValue = 'A';
            Character charObject = charValue; // Auto-boxing
    
            boolean booleanValue = true;
            Boolean booleanObject = booleanValue; // Auto-boxing
    
            // Displaying the converted objects
            System.out.println("Integer Object: " + integerObject);
            System.out.println("Double Object: " + doubleObject);
            System.out.println("Character Object: " + charObject);
            System.out.println("Boolean Object: " + booleanObject);
        }
    }
    


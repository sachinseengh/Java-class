public class Q6_Equals {
    public static void main(String[] args) {

        String s1 = "Sachin";
        String s2 = "Singh";

        boolean Result = s1.equals(s2);

        if (Result == true) {
            System.out.println("Both Strings are equal");
        } else {
            System.out.println("Strings are not equal");
        }

    }
}
